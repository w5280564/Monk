package com.qingbo.monk.bean;

public class HaveBean {


    private String id;
    private String name;
    private String image;
    private boolean isCheck;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public boolean isCheck() {
        return isCheck;
    }

    public void setCheck(boolean check) {
        isCheck = check;
    }
}
