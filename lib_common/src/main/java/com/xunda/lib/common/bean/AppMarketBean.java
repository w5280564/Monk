package com.xunda.lib.common.bean;

public class AppMarketBean {
    private String marketName;
    private String BrandName;
    private String marketPakageName;
    private int iconResource;

    public String getMarketName() {
        return marketName;
    }

    public void setMarketName(String marketName) {
        this.marketName = marketName;
    }

    public String getMarketPakageName() {
        return marketPakageName;
    }

    public void setMarketPakageName(String marketPakageName) {
        this.marketPakageName = marketPakageName;
    }

    public int getIconResource() {
        return iconResource;
    }

    public void setIconResource(int iconResource) {
        this.iconResource = iconResource;
    }

    public String getBrandName() {
        return BrandName;
    }

    public void setBrandName(String brandName) {
        BrandName = brandName;
    }
}
